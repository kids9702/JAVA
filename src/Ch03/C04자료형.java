package Ch03;

public class C04자료형 {

	public static void main(String[] args) {
		
		//변수 기본
		
		//선언 : 이름부여 
		//정의 : 이름부여 + 공간형성
		//초기화 : 이름부여 + 공간형성 + 초기값 
		
		int num1=0; // 4byte 공간형성후 num1 이름 부여(정의)
		num1 = 10; //10리터럴상수를 num1공간에 대입 
		
		int num2 = 4; // 4리터럴상수를 4byte공간num2에 초기화
		int num3 = num1 + num2; // num1값 +num2값을 4byte num3에 초기화
		
		System.out.println(num3); // num3의 값을 10진정수로 출력
		
		//깃허브 테스트 주석
		//깃허브 테스트 주석2

	}

}
